const categories = [
  "bread",
  "cookieSmallSweets",
  "cakeMadeleine",
  "giftSet",
  "jamSpread",
  "cashewProduct",
];

const htmlRoot = document.documentElement;
const languageSelect = document.querySelector("#languageSelect");
const categoryTabs = document.querySelector("#categoryTabs");
const menuGrid = document.querySelector("#menuGrid");
const menuCardTemplate = document.querySelector("#menuCardTemplate");

let activeLanguage = "en";
let activeCategory = categories[0];

function formatVnd(price) {
  return `${price.toLocaleString("en-US")} VND`;
}

function t(key) {
  return i18n[activeLanguage][key] ?? i18n.en[key] ?? key;
}

function toIncludedText(value) {
  return value ? t("included") : t("excluded");
}

function getAllergyEmoji(value) {
  return value ? "⭕" : "❌";
}

function getFlavorIcon(flavor) {
  if (flavor.includes("바닐라")) return "🤍";
  if (flavor.includes("초콜릿")) return "🍫";
  if (flavor.includes("코코넛")) return "🥥";
  if (flavor.includes("얼그레이")) return "🫖";
  if (flavor.includes("커피")) return "☕";
  return "✨";
}

function getFlavorLabel(flavor) {
  const flavorI18n = {
    바닐라: { en: "Vanilla", vi: "Vani", ja: "バニラ", zh: "香草" },
    초콜릿: { en: "Chocolate", vi: "Socola", ja: "チョコレート", zh: "巧克力" },
    코코넛: { en: "Coconut", vi: "Dừa", ja: "ココナッツ", zh: "椰子" },
    얼그레이: { en: "Earl Grey", vi: "Trà Earl Grey", ja: "アールグレイ", zh: "伯爵茶" },
    커피: { en: "Coffee", vi: "Cà phê", ja: "コーヒー", zh: "咖啡" },
  };

  const labels = flavorI18n[flavor];
  return labels?.[activeLanguage] ?? flavor;
}

function updateStaticTexts() {
  htmlRoot.lang = activeLanguage;
  const textElements = document.querySelectorAll("[data-i18n]");
  textElements.forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = t(key);
  });
}

function renderTabs() {
  categoryTabs.innerHTML = "";
  categories.forEach((category) => {
    const button = document.createElement("button");
    button.type = "button";
    button.className = "tab-button";
    button.setAttribute("role", "tab");
    button.setAttribute("aria-selected", String(category === activeCategory));
    button.textContent = t("categories")[category];
    if (category === activeCategory) button.classList.add("is-active");
    button.addEventListener("click", () => {
      activeCategory = category;
      renderTabs();
      renderMenus();
    });
    categoryTabs.append(button);
  });
}

function renderMenus() {
  menuGrid.innerHTML = "";
  const items = menuData.filter((menu) => menu.category === activeCategory);
  items.forEach((menu) => {
    const card = menuCardTemplate.content.firstElementChild.cloneNode(true);

    // 일부 메뉴는 flavor 칩 줄바꿈으로 가격 baseline이 달라지는 현상이 있어,
    // 해당 메뉴만 flavors 영역 최소 높이를 고정해 가격 위치를 맞춥니다.
    const koName = menu.name?.ko ?? "";
    if (koName === "캐슈볼쿠키 L" || koName === "캐슈볼쿠키 S" || koName === "캐슈크런치토피") {
      card.style.setProperty("--flavors-min-height", "40px");
    }

    // 잼 카테고리는 description 줄바꿈에 따라 카드 높이가 달라져
    // 여러 행에서 가격이 일직선으로 보이지 않는 경우가 있어 카드 최소 높이를 고정합니다.
    if (menu.category === "jamSpread") {
      card.classList.add("is-jamSpread");
      // min-height 고정만으로는 description 줄 수 편차가 남는 경우가 있어,
      // CSS에서 description을 라인 클램프 처리합니다.
      card.style.setProperty("--menu-card-min-height", "auto");
      card.style.setProperty("--menu-card-min-height-mobile", "auto");
    }

    const imageElement = card.querySelector(".menu-card__image");
    const flavorContainer = card.querySelector(".menu-card__flavors");
    const allergyFields = card.querySelectorAll(".menu-card__allergy");
    const menuName = menu.name[activeLanguage] ?? menu.name.en;
    const menuDescription = menu.description[activeLanguage] ?? menu.description.en;
    const resolvedImageSrc = new URL(menu.image, window.location.href).href;
    const fallbackImageSrc = new URL("images/menu-01.png", window.location.href).href;

    imageElement.src = resolvedImageSrc;
    imageElement.alt = `${menuName} image`;
    imageElement.decoding = "async";
    imageElement.loading = "eager";
    imageElement.style.setProperty("--image-scale", String(menu.imageScale ?? 1));
    imageElement.style.setProperty("--image-rotate", String(menu.imageRotate ?? "0deg"));
    imageElement.style.setProperty("--image-offset-y", String(menu.imageOffsetY ?? "0px"));
    imageElement.onerror = () => {
      imageElement.onerror = null;
      imageElement.src = fallbackImageSrc;
    };

    card.querySelector(".menu-card__name").textContent = menuName;
    card.querySelector(".menu-card__description").textContent = menuDescription;
    flavorContainer.innerHTML = "";
    if (Array.isArray(menu.flavors) && menu.flavors.length > 0) {
      menu.flavors.forEach((flavor) => {
        const chip = document.createElement("span");
        chip.className = "flavor-chip";
        chip.textContent = `${getFlavorIcon(flavor)} ${getFlavorLabel(flavor)}`;
        flavorContainer.append(chip);
      });
    }
    card.querySelector(".menu-card__price").textContent = formatVnd(menu.priceVnd);
    allergyFields[0].innerHTML = `NUTS <span class="allergy-status ${menu.nutsIncluded ? "is-included" : "is-excluded"}" aria-label="${toIncludedText(menu.nutsIncluded)}">${getAllergyEmoji(menu.nutsIncluded)}</span>`;
    allergyFields[1].innerHTML = `EGG <span class="allergy-status ${menu.eggIncluded ? "is-included" : "is-excluded"}" aria-label="${toIncludedText(menu.eggIncluded)}">${getAllergyEmoji(menu.eggIncluded)}</span>`;
    const hasEggWash = menu.eggWashIncluded ?? false;
    allergyFields[2].innerHTML = `EGG WASH <span class="allergy-status ${hasEggWash ? "is-included" : "is-excluded"}" aria-label="${toIncludedText(hasEggWash)}">${getAllergyEmoji(hasEggWash)}</span>`;
    menuGrid.append(card);
  });
}

